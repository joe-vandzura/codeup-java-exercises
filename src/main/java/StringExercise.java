public class StringExercise {
    public static void main(String[] args) {

        String pinkFloyd = "We don't need no education\nWe don't need no thought control\n";
        System.out.println(pinkFloyd);

        String checkThisOut = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(checkThisOut);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String backSlashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!\n";
        System.out.println(backSlashes);

    }
}
